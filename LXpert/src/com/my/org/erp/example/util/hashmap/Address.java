package com.my.org.erp.example.util.hashmap;

public class Address 
{
	String name;
	int age;
	Address(String name,int age)
	{
		this.name =name;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
